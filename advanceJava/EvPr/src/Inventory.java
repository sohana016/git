        public void showData(){
            String [] items = new String[products.size()];
            for(int i = 0;i< products.size();i++){
                items[i] = products.get(i).getName();
            
            }
        comboBox.setModel(new javax.swing.DefaultComboBoxModel<>(items));
        
        }
        
        public void showTable(){
            Object[][] items = new Object[products.size()][3];
                for(int i = 0;i<products.size();i++){
                    Product p = products.get(i);
                    items[i][0] = p.getName();
                    items[i][1] = p.getQunatity();
                    items[i][2] = p.getPrice();
                    
                
                
                }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
    items,
        
    
    new String [] {
        "Name", "Quantity", "Price"
    }
));
        
        
        }
        
