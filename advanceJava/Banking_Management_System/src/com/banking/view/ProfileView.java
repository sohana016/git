    private void save1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save1ActionPerformed
        // TODO add your handling code here:
        String name = inp_name1.getText();
        String dateofB = inp_Bday.getText();
        
       String nationality = inp_natnalty.getText();
       String gender = inp_gendr.getText();
       String address = inp_addrss.getText();
       String accNo = inp_accNo.getText();
       String accTyp = inp_acctyp.getText();
       String caste = inp_caste.getText();
       int mobile = Integer.parseInt(inp_mbl.getText());
       String seq = inp_seqQ.getText();
       
        try {
             Profile profile = new Profile(name, dateofB, nationality, address, accNo, accTyp, caste, seq, mobile, gender);
            
            ProfileImp proImp = new ProfileImp();
            proImp.update(profile);
            
        } catch (Exception e) {
        }
       
      
    }//GEN-LAST:event_save1ActionPerformed

